// Math.E

/*
    Math.E представляє основу натурального логарифму.

    Натуральний логарифм — це логарифм за основою e,
    Де e — це особлива константа, приблизно рівна 2.71828. Відома вона як число Ейлера.
    Натуральний логарифм числа записується як ln x
 */

console.log(Math.E) // 2.718281828459045


